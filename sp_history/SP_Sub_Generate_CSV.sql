				SET A.END_CALL_COUNT=B.END_CALL_COUNT
					,A.END_PILOT_RSCP=B.END_PILOT_RSCP
					,A.END_PILOT_ECN0=B.END_PILOT_ECN0
					,A.IRAT_ATT=B.IRAT_ATT
					,A.IRAT_SUCCESS_RATE=(B.IRAT_SUCCESS/B.IRAT_ATT)
				WHERE A.RNC_ID=B.RNC_ID AND A.CELL_ID=B.CELL_ID AND A.SERVICETYPE=B.SERVICETYPE;');
	PREPARE Stmt FROM @SqlCmd;
	EXECUTE Stmt;
	DEALLOCATE PREPARE Stmt;
	
	SET @SqlCmd=CONCAT('UPDATE ',GT_DB,'.`tmp_huawei_csv','_',WORKER_ID,'` a,',GT_DB,'.`tmp_table_fp','_',WORKER_ID,'` B
				SET A.MR_COUNT=B.MR_COUNT
					,A.FP_PILOT_RSCP=B.FP_PILOT_RSCP
					,A.FP_PILOT_ECN0=B.FP_PILOT_ECN0
					,A.AS_COUNT=B.AS_COUNT
					,A.POLLUTER=B.POLLUTER
					,A.POLLUTED_MR=B.POLLUTED_MR
				WHERE A.RNC_ID=B.RNC_ID AND A.CELL_ID=B.CELL_ID AND A.SERVICETYPE=B.SERVICETYPE;');
	PREPARE Stmt FROM @SqlCmd;
	EXECUTE Stmt;
	DEALLOCATE PREPARE Stmt;
	
	SET @SqlCmd=CONCAT('UPDATE ',GT_DB,'.`tmp_huawei_csv','_',WORKER_ID,'` a
				JOIN  ',GT_DB,'.NT_CURRENT c
				ON A.RNC_ID=c.RNC_ID AND A.CELL_ID=c.CELL_ID
				JOIN ',GT_DB,'.NT_RNC_CURRENT B
				ON c.RNC_ID=B.RNC_ID				
				SET A.SAI=CONCAT(CONVERT(MCC USING utf8) ,CONVERT(B.MNC USING utf8) ,RIGHT(CONCAT(''0000'',HEX(c.LAC)),4),RIGHT(CONCAT(''0000'',HEX(c.CELL_ID)),4))
					,RNC_ID_NEW=CONCAT(CONVERT(MCC USING utf8),''-'' ,CONVERT(B.MNC USING utf8),''-'' ,RIGHT(CONCAT(''00000'',A.RNC_ID),5)) ;');
	PREPARE Stmt FROM @SqlCmd;
	EXECUTE Stmt;
	DEALLOCATE PREPARE Stmt;
	
	INSERT INTO gt_gw_main.sp_log VALUES(GT_DB,'SP_Sub_Generate_CSV','step 8', NOW());
	
	SET @SqlCmd=CONCAT('SELECT ''RNC_ID'',''CELL_ID'',''SAI'',''TIMESTAMP'',
				''INIT_CALL_COUNT'',''INIT_RSCP'',''INIT_PILOT_RSCP'',''INIT_ECN0'',''INIT_PILOT_ECN0'',
				''DL_VOL'',''UL_VOL'',''PS_CALL_COUNT'',''DL_THRO'',''UL_THRO'',''MAX_DL_THRO'',''MAX_UL_THRO'',
