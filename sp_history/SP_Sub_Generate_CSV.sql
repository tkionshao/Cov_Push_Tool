	SET @SqlCmd=CONCAT('INSERT INTO ',GT_DB,'.`tmp_huawei_csv','_',WORKER_ID,'` ( RNC_ID,CELL_ID,SERVICETYPE,`TIMESTAMP`)
				SELECT RNC_ID,CELL_ID,SERVICETYPE,UNIX_TIMESTAMP(''',GT_START_TIME,''')
				FROM 
				(
					SELECT RNC_ID,CELL_ID,SERVICETYPE FROM ',GT_DB,'.`tmp_table_first','_',WORKER_ID,'`
					UNION 
					SELECT RNC_ID,CELL_ID,SERVICETYPE FROM ',GT_DB,'.`tmp_table_ifho','_',WORKER_ID,'`
					UNION 
					SELECT RNC_ID,CELL_ID,SERVICETYPE FROM ',GT_DB,'.`tmp_table_as1','_',WORKER_ID,'`
					UNION 
					SELECT RNC_ID,CELL_ID,SERVICETYPE FROM ',GT_DB,'.`tmp_table_last','_',WORKER_ID,'`
					UNION 
					SELECT RNC_ID,CELL_ID,SERVICETYPE FROM ',GT_DB,'.`tmp_table_fp','_',WORKER_ID,'`
				) AA;');
	PREPARE Stmt FROM @SqlCmd;
	EXECUTE Stmt;
	DEALLOCATE PREPARE Stmt;
	
	INSERT INTO gt_gw_main.sp_log VALUES(GT_DB,'SP_Sub_Generate_CSV','step 7', NOW());
	
	SET @SqlCmd=CONCAT('UPDATE ',GT_DB,'.`tmp_huawei_csv','_',WORKER_ID,'` a,',GT_DB,'.`tmp_table_first','_',WORKER_ID,'` B
				SET A.SUBSCRIBER=B.SUBSCRIBER
					,A.INIT_CALL_COUNT=B.INIT_CALL_COUNT
					,A.INIT_RSCP=B.INIT_RSCP
					,A.INIT_ECN0=B.INIT_ECN0
					,A.DL_VOL=B.DL_VOL
					,A.UL_VOL=B.UL_VOL
					,A.PS_CALL_COUNT=B.PS_CALL_COUNT
					,A.DL_THRO=B.DL_THRO
					,A.UL_THRO=B.UL_THRO
					,A.MAX_DL_THRO=B.MAX_DL_THRO
					,A.MAX_UL_THRO=B.MAX_UL_THRO
					,A.BLOCK_RATE=B.BLOCK/B.INIT_CALL_COUNT
					,A.INIT_PILOT_RSCP=B.INIT_PILOT_RSCP
					,A.INIT_PILOT_ECN0=B.INIT_PILOT_ECN0
					,A.RRC_SUCCESS_RATE=1-(B.BLOCK/B.INIT_CALL_COUNT)
				WHERE A.RNC_ID=B.RNC_ID AND A.CELL_ID=B.CELL_ID AND A.SERVICETYPE=B.SERVICETYPE;');
	PREPARE Stmt FROM @SqlCmd;
	EXECUTE Stmt;
	DEALLOCATE PREPARE Stmt;
	
	SET @SqlCmd=CONCAT('UPDATE ',GT_DB,'.`tmp_huawei_csv','_',WORKER_ID,'` a,',GT_DB,'.`tmp_table_ifho','_',WORKER_ID,'` B
				SET 
					A.IFHO_COUNT=B.IFHO_COUNT
				WHERE A.RNC_ID=B.RNC_ID AND A.CELL_ID=B.CELL_ID AND A.SERVICETYPE=B.SERVICETYPE;');
	PREPARE Stmt FROM @SqlCmd;
	EXECUTE Stmt;
	DEALLOCATE PREPARE Stmt;
	
	SET @SqlCmd=CONCAT('UPDATE ',GT_DB,'.`tmp_huawei_csv','_',WORKER_ID,'`
				SET 
					IFHO_RATE=IFHO_COUNT/INIT_CALL_COUNT
				WHERE IFHO_COUNT IS NOT NULL AND INIT_CALL_COUNT IS NOT NULL;');
	PREPARE Stmt FROM @SqlCmd;
	EXECUTE Stmt;
	DEALLOCATE PREPARE Stmt;
	
	SET @SqlCmd=CONCAT('UPDATE ',GT_DB,'.`tmp_huawei_csv','_',WORKER_ID,'` a,',GT_DB,'.`tmp_table_as1','_',WORKER_ID,'` B
				SET A.INIT_SERV_RSCP=B.INIT_SERV_RSCP
					,A.INIT_SERV_ECN0=B.INIT_SERV_ECN0
					,A.ACT_CALL_COUNT=B.ACT_CALL_COUNT
				WHERE A.RNC_ID=B.RNC_ID AND A.CELL_ID=B.CELL_ID AND A.SERVICETYPE=B.SERVICETYPE;');
	PREPARE Stmt FROM @SqlCmd;
	EXECUTE Stmt;
	DEALLOCATE PREPARE Stmt;
	
