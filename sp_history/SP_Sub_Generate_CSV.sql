	PREPARE Stmt FROM @SqlCmd;
	EXECUTE Stmt;
	DEALLOCATE PREPARE Stmt;
	SET @SqlCmd=CONCAT('SELECT att_value INTO @pilot_ecn0 FROM ',CURRENT_NT_DB,'.`sys_config` WHERE `group_name`=''pilot'' AND att_name = ''pilot_ecn0'';');
	PREPARE Stmt FROM @SqlCmd;
	EXECUTE Stmt;
	DEALLOCATE PREPARE Stmt;
	
	SET @SqlCmd=CONCAT('SELECT att_value INTO @pilot_rscp_delta FROM ',CURRENT_NT_DB,'.`sys_config` WHERE `group_name`=''pilot'' AND att_name = ''pilot_rscp_delta'';');
	PREPARE Stmt FROM @SqlCmd;
	EXECUTE Stmt;
	DEALLOCATE PREPARE Stmt;
	
	SET @SqlCmd=CONCAT('SELECT att_value INTO @pilot_pollution_trigger FROM ',CURRENT_NT_DB,'.`sys_config` WHERE `group_name`=''pilot'' AND att_name = ''pilot_pollution_trigger'';');
	PREPARE Stmt FROM @SqlCmd;
	EXECUTE Stmt;
	DEALLOCATE PREPARE Stmt;
	
        SELECT CONCAT(LEFT(gt_strtok(GT_DB,3,'_'),4),'-',SUBSTRING(gt_strtok(GT_DB,3,'_'),5,2),'-',SUBSTRING(gt_strtok(GT_DB,3,'_'),7,2),' ',LEFT(gt_strtok(GT_DB,4,'_'),2),':',RIGHT(gt_strtok(GT_DB,4,'_'),2),':00') INTO GT_START_TIME;
 	SELECT SUBSTRING(SUBSTRING(GT_DB, 4,LENGTH(GT_DB)-4),1, LOCATE('_', SUBSTRING(GT_DB, 4,LENGTH(GT_DB)-4))-1) INTO RNC_ID;
 
	
	
	INSERT INTO gt_gw_main.sp_log VALUES(GT_DB,'SP_Sub_Generate_CSV','Insert data to table_tile_fp_15-Start ', NOW());
	SET @SqlCmd=CONCAT('DROP TEMPORARY TABLE IF EXISTS  ',GT_DB,'.tmp_table_call','_',WORKER_ID,' ;');
	PREPARE Stmt FROM @SqlCmd;
	EXECUTE Stmt;
	DEALLOCATE PREPARE Stmt;	
	SET @SqlCmd=CONCAT('CREATE TEMPORARY TABLE ',GT_DB,'.`tmp_table_call','_',WORKER_ID,'`  ENGINE=MYISAM
				SELECT 
				CALL_ID
				,POS_FIRST_RNC
				,POS_FIRST_CELL 
				,CALL_TYPE
				,CALL_STATUS
				,IMSI
				,NULL AS POS_IFHO_RNC
				,NULL AS POS_IFHO_CELL
				,POS_FIRST_RSCP
				,POS_FIRST_ECN0
				,DL_TRAFFIC_VOLUME
				,UL_TRAFFIC_VOLUME
				,DL_THROUGHPUT_AVG
				,UL_THROUGHPUT_AVG
				,SIMULATED
				,POS_AS1_RNC
				,POS_AS1_CELL
				,POS_AS1_RSCP
				,POS_AS1_ECN0
				,POS_LAST_RNC
				,POS_LAST_CELL
				,POS_LAST_RSCP
				,POS_LAST_ECN0
				,IRAT_HHO_ATTEMPT
				,IRAT_HHO_SUCCESS
				,DATA_HOUR
				,DATA_DATE
				,POS_FIRST_FREQUENCY
				,POS_FIRST_UARFCN
				,INDOOR
				,MOVING
				,POS_FIRST_LOC
				,POS_FIRST_CELL_INDOOR
				,POS_FIRST_CLUSTER
				,POS_FIRST_SITE
				,RRC_CONNECT_DURATION
				,POS_LAST_LOC
				,POS_LAST_CELL_INDOOR
				,POS_LAST_CLUSTER
				,POS_LAST_SITE
				,POS_LAST_FREQUENCY
				,POS_LAST_UARFCN
				,IU_RELEASE_CAUSE
				FROM ',GT_DB,'.table_call;
				');
	PREPARE Stmt FROM @SqlCmd;
	EXECUTE Stmt;
	DEALLOCATE PREPARE Stmt;
	
	SET @SqlCmd=CONCAT('CREATE INDEX CALL_ID on ',GT_DB,'.`tmp_table_call','_',WORKER_ID,'` (CALL_ID)   ;');
	PREPARE Stmt FROM @SqlCmd;
	EXECUTE Stmt;
	DEALLOCATE PREPARE Stmt;
	
	INSERT INTO gt_gw_main.sp_log VALUES(GT_DB,'SP_Sub_Generate_CSV','step 1', NOW());
	
	SET @SqlCmd=CONCAT('DROP TEMPORARY TABLE IF EXISTS  ',GT_DB,'.`tmp_table_first','_',WORKER_ID,'` ;');
	PREPARE Stmt FROM @SqlCmd;
	EXECUTE Stmt;
	DEALLOCATE PREPARE Stmt;	
	
	SET @SqlCmd=CONCAT('CREATE TEMPORARY TABLE ',GT_DB,'.`tmp_table_first','_',WORKER_ID,'` ENGINE=MYISAM
				SELECT POS_FIRST_RNC RNC_ID 
				,POS_FIRST_CELL CELL_ID 
				,CASE WHEN CALL_TYPE IN (10) THEN ''Voice'' WHEN CALL_TYPE IN (12,13,14,18) THEN ''Data'' WHEN CALL_TYPE =16 THEN ''SMS'' END AS SERVICETYPE
				,COUNT(DISTINCT IMSI) AS subscriber
				,COUNT(POS_FIRST_CELL) AS init_call_count
				,AVG(POS_FIRST_RSCP) AS init_rscp
				,AVG(POS_FIRST_ECN0) AS init_ecn0
				,SUM(DL_TRAFFIC_VOLUME) AS dl_vol
				,SUM(UL_TRAFFIC_VOLUME) AS ul_vol
				,SUM(CASE WHEN CALL_TYPE IN (12,13,14,18) THEN 1 ELSE 0 END) AS ps_call_count
				,AVG(CASE WHEN CALL_TYPE IN (12,13,14,18) THEN DL_THROUGHPUT_AVG ELSE NULL END) AS dl_thro
				,AVG(CASE WHEN CALL_TYPE IN (12,13,14,18) THEN UL_THROUGHPUT_AVG ELSE NULL END) AS ul_thro
				,MAX(DL_THROUGHPUT_AVG) AS max_dl_thro
				,MAX(UL_THROUGHPUT_AVG) AS max_ul_thro
				,SUM(CASE CALL_STATUS WHEN 3 THEN 1 ELSE 0 END) AS block
				,AVG(CASE `SIMULATED` WHEN 0 THEN POS_FIRST_RSCP ELSE NULL END) AS init_pilot_rscp
				,AVG(CASE `SIMULATED` WHEN 0 THEN POS_FIRST_ECN0 ELSE NULL END) AS init_pilot_ecn0
				FROM ',GT_DB,'.`tmp_table_call','_',WORKER_ID,'`
				WHERE POS_FIRST_CELL IS NOT NULL AND POS_FIRST_RSCP IS NOT NULL AND DATA_HOUR=',START_HH,'
				GROUP BY POS_FIRST_RNC,POS_FIRST_CELL,SERVICETYPE;');
	PREPARE Stmt FROM @SqlCmd;
	EXECUTE Stmt;
	DEALLOCATE PREPARE Stmt;
	
	INSERT INTO gt_gw_main.sp_log VALUES(GT_DB,'SP_Sub_Generate_CSV','step 2', NOW());
	
	SET @SqlCmd=CONCAT('DROP TEMPORARY TABLE IF EXISTS  ',GT_DB,'.`tmp_table_ifho','_',WORKER_ID,'` ;');
	PREPARE Stmt FROM @SqlCmd;
	EXECUTE Stmt;
	DEALLOCATE PREPARE Stmt;	
	
	SET @SqlCmd=CONCAT('CREATE TEMPORARY TABLE ',GT_DB,'.`tmp_table_ifho','_',WORKER_ID,'` ENGINE=MYISAM
				SELECT 
				POS_IFHO_RNC RNC_ID 
				,POS_IFHO_CELL CELL_ID 
				,CASE WHEN CALL_TYPE IN (10) THEN ''Voice'' WHEN CALL_TYPE IN (12,13,14,18) THEN ''Data'' WHEN CALL_TYPE =16 THEN ''SMS'' END AS SERVICETYPE
				,COUNT(POS_IFHO_CELL) AS IFHO_COUNT
				FROM ',GT_DB,'.`tmp_table_call','_',WORKER_ID,'`
				WHERE POS_IFHO_RNC =',RNC_ID,' AND DATA_HOUR=',START_HH,'
				GROUP BY POS_IFHO_RNC,POS_IFHO_CELL,SERVICETYPE;');
	PREPARE Stmt FROM @SqlCmd;
	EXECUTE Stmt;
	DEALLOCATE PREPARE Stmt;
	
	INSERT INTO gt_gw_main.sp_log VALUES(GT_DB,'SP_Sub_Generate_CSV','step 2', NOW());
	
	SET @SqlCmd=CONCAT('DROP TEMPORARY TABLE IF EXISTS  ',GT_DB,'.`tmp_table_as1','_',WORKER_ID,'` ;');
	PREPARE Stmt FROM @SqlCmd;
	EXECUTE Stmt;
	DEALLOCATE PREPARE Stmt;
	
	SET @SqlCmd=CONCAT('CREATE TEMPORARY TABLE ',GT_DB,'.`tmp_table_as1','_',WORKER_ID,'` ENGINE=MYISAM
				SELECT POS_AS1_RNC RNC_ID
					,POS_AS1_CELL CELL_ID
					,CASE WHEN CALL_TYPE IN (10) THEN ''Voice'' WHEN CALL_TYPE IN (12,13,14,18) THEN ''Data'' WHEN CALL_TYPE =16 THEN ''SMS'' END AS SERVICETYPE
					,COUNT(POS_AS1_RSCP) AS act_call_count
					,AVG(POS_AS1_RSCP) AS init_serv_rscp
					,AVG(POS_AS1_ECN0) AS init_serv_ecn0
				FROM ',GT_DB,'.`tmp_table_call','_',WORKER_ID,'`
				WHERE POS_AS1_CELL IS NOT NULL AND POS_AS1_RSCP IS NOT NULL AND DATA_HOUR=',START_HH,'
				GROUP BY POS_AS1_RNC,POS_AS1_CELL,SERVICETYPE;');
	PREPARE Stmt FROM @SqlCmd;
	EXECUTE Stmt;
	DEALLOCATE PREPARE Stmt;
	
	INSERT INTO gt_gw_main.sp_log VALUES(GT_DB,'SP_Sub_Generate_CSV','step 3', NOW());
	
	SET @SqlCmd=CONCAT('DROP TEMPORARY TABLE IF EXISTS  ',GT_DB,'.`tmp_table_last','_',WORKER_ID,'` ;');
	PREPARE Stmt FROM @SqlCmd;
	EXECUTE Stmt;
	DEALLOCATE PREPARE Stmt;
	
	SET @SqlCmd=CONCAT('CREATE TEMPORARY TABLE ',GT_DB,'.`tmp_table_last','_',WORKER_ID,'` ENGINE=MYISAM
				SELECT POS_LAST_RNC AS RNC_ID 
					,POS_LAST_CELL AS CELL_ID 
					,CASE WHEN CALL_TYPE IN (10) THEN ''Voice'' WHEN CALL_TYPE IN (12,13,14,18) THEN ''Data'' WHEN CALL_TYPE =16 THEN ''SMS'' END AS SERVICETYPE
					,COUNT(POS_LAST_CELL) AS end_call_count
					,AVG(POS_LAST_RSCP) AS end_pilot_rscp
					,AVG(POS_LAST_ECN0) AS end_pilot_ecn0
					,SUM(IRAT_HHO_ATTEMPT) AS irat_att
					,SUM(IRAT_HHO_SUCCESS) AS irat_success
				FROM ',GT_DB,'.`tmp_table_call','_',WORKER_ID,'`
				WHERE POS_LAST_CELL IS NOT NULL AND DATA_HOUR=',START_HH,'
				GROUP BY POS_LAST_RNC,POS_LAST_CELL,SERVICETYPE;');
	PREPARE Stmt FROM @SqlCmd;
	EXECUTE Stmt;
	DEALLOCATE PREPARE Stmt;
	
	INSERT INTO gt_gw_main.sp_log VALUES(GT_DB,'SP_Sub_Generate_CSV','step 4', NOW());
	
	SET @SqlCmd=CONCAT('DROP TEMPORARY TABLE IF EXISTS  ',GT_DB,'.`tmp_table_fp','_',WORKER_ID,'` ;');
	PREPARE Stmt FROM @SqlCmd;
	EXECUTE Stmt;
	DEALLOCATE PREPARE Stmt;
	
	SET @SqlCmd=CONCAT('CREATE TEMPORARY TABLE ',GT_DB,'.`tmp_table_fp','_',WORKER_ID,'` ENGINE=MYISAM
				SELECT  gt_covmo_csv_get(RNC_ID,1) RNC_ID
					, gt_covmo_csv_get(CELL_ID,1) CELL_ID
					,CASE WHEN A.CALL_TYPE IN (10) THEN ''Voice'' WHEN A.CALL_TYPE IN (12,13,14,18) THEN ''Data'' WHEN A.CALL_TYPE =16 THEN ''SMS'' END AS SERVICETYPE
					, COUNT(*) AS mr_count
					, AVG(gt_covmo_csv_get(RSCP,1)) AS fp_pilot_rscp
					, AVG(gt_covmo_csv_get(ECN0,1)) AS fp_pilot_ecn0
					, AVG(gt_covmo_csv_count(ACTIVE_SET_CELL_ID, '','')) AS as_count
					, SUM(IF(gt_covmo_polluter(RSCP, ECN0,',@pilot_rscp,',',@pilot_ecn0,',',@pilot_rscp_delta,')>',@pilot_pollution_trigger,',gt_covmo_polluter(RSCP, ECN0,',@pilot_rscp,',',@pilot_ecn0,',',@pilot_rscp_delta,')-',@pilot_pollution_trigger,',0)) AS POLLUTER
					, SUM(IF(gt_covmo_polluter(RSCP, ECN0,',@pilot_rscp,',',@pilot_ecn0,',',@pilot_rscp_delta,')>',@pilot_pollution_trigger,',1,0)) AS polluted_mr	
				FROM ',GT_DB,'.`tmp_table_call','_',WORKER_ID,'` A  FORCE INDEX (CALL_ID), ',GT_DB,'.table_position B FORCE INDEX (CALL_ID)
				WHERE A.CALL_ID=B.CALL_ID
				AND gt_covmo_csv_getnum(B.RNC_ID,1)=',RNC_ID,'
				AND RSCP IS NOT NULL AND A.DATA_HOUR=',START_HH,' AND B.DATA_HOUR=',START_HH,'
				GROUP BY gt_covmo_csv_get(RNC_ID,1),gt_covmo_csv_get(CELL_ID,1),SERVICETYPE
				ORDER BY NULL;');
	PREPARE Stmt FROM @SqlCmd;
	EXECUTE Stmt;
	DEALLOCATE PREPARE Stmt;
	
	INSERT INTO gt_gw_main.sp_log VALUES(GT_DB,'SP_Sub_Generate_CSV','step 5', NOW());
	
	SET @SqlCmd=CONCAT('DROP TEMPORARY TABLE IF EXISTS  ',GT_DB,'.`tmp_huawei_csv','_',WORKER_ID,'` ;');
	PREPARE Stmt FROM @SqlCmd;
	EXECUTE Stmt;
	DEALLOCATE PREPARE Stmt;
	
	SET @SqlCmd=CONCAT('CREATE TEMPORARY TABLE ',GT_DB,'.`tmp_huawei_csv','_',WORKER_ID,'`
				(
					RNC_ID	VARCHAR(50)  DEFAULT NULL,
					CELL_ID	VARCHAR(50)  DEFAULT NULL,
					SAI	VARCHAR(50)  DEFAULT NULL,
					`TIMESTAMP` BIGINT(20) DEFAULT NULL,
					INIT_CALL_COUNT	INT  DEFAULT NULL,
					INIT_RSCP	DOUBLE  DEFAULT NULL,
					INIT_PILOT_RSCP	DOUBLE  DEFAULT NULL,
					INIT_ECN0	DOUBLE  DEFAULT NULL,
					INIT_PILOT_ECN0	DOUBLE  DEFAULT NULL,
					DL_VOL	DOUBLE  DEFAULT NULL,
					UL_VOL	DOUBLE  DEFAULT NULL,
					PS_CALL_COUNT	INT  DEFAULT NULL,
					DL_THRO	DOUBLE  DEFAULT NULL,
					UL_THRO	DOUBLE  DEFAULT NULL,
					MAX_DL_THRO	DOUBLE  DEFAULT NULL,
					MAX_UL_THRO	DOUBLE  DEFAULT NULL,
					SUBSCRIBER	INT  DEFAULT NULL,
					BLOCK_RATE	DOUBLE  DEFAULT NULL,
					RRC_SUCCESS_RATE	INT  DEFAULT NULL,
					ACT_CALL_COUNT	INT  DEFAULT NULL,
					INIT_SERV_RSCP	DOUBLE  DEFAULT NULL,
					INIT_SERV_ECN0	DOUBLE  DEFAULT NULL,
					IFHO_COUNT	INT  DEFAULT NULL,
					IFHO_RATE	DOUBLE  DEFAULT NULL,
					END_CALL_COUNT	INT  DEFAULT NULL,
					END_PILOT_RSCP	DOUBLE  DEFAULT NULL,
					END_PILOT_ECN0	DOUBLE  DEFAULT NULL,
					IRAT_ATT	INT  DEFAULT NULL,
					IRAT_SUCCESS_RATE	DOUBLE  DEFAULT NULL,
					MR_COUNT	INT  DEFAULT NULL,
					FP_PILOT_RSCP	DOUBLE  DEFAULT NULL,
					FP_PILOT_ECN0	DOUBLE  DEFAULT NULL,
					POLLUTED_MR	DOUBLE  DEFAULT NULL,
					POLLUTER	DOUBLE  DEFAULT NULL,
					AS_COUNT	DOUBLE  DEFAULT NULL,
					RNC_ID_NEW	VARCHAR(50)  DEFAULT NULL,
					SERVICETYPE	CHAR(5)  DEFAULT NULL,
					RESERVED1	VARCHAR(20)  DEFAULT NULL,
					RESERVED2	VARCHAR(20)  DEFAULT NULL,
					RESERVED3	VARCHAR(20)  DEFAULT NULL,
					RESERVED4	VARCHAR(20)  DEFAULT NULL,
					RESERVED5	VARCHAR(20)  DEFAULT NULL,
					RESERVED6	DOUBLE  DEFAULT NULL,
					RESERVED7	DOUBLE  DEFAULT NULL,
					RESERVED8	DOUBLE  DEFAULT NULL,
					RESERVED9	DOUBLE  DEFAULT NULL,
					RESERVED10	DOUBLE  DEFAULT NULL
				) ENGINE=MYISAM DEFAULT CHARSET=LATIN1;');
	PREPARE Stmt FROM @SqlCmd;
	EXECUTE Stmt;
	DEALLOCATE PREPARE Stmt;
	
